  // Map to quickly lookup by any callsign prefix
  static final Map<String, String> _callsignToPrefix = {};

  static void _initializeCallsignMap() {
    if (_callsignToPrefix.isNotEmpty) return;

    for (final entry in _countryData.entries) {
      final prefix = entry.key;
      final data = entry.value;

      for (final callsign in data.callsigns) {
        _callsignToPrefix[callsign.toUpperCase()] = prefix;
      }
    }
  }

  /// Get ITU zone for a given callsign
  static String? getItuZone(String callsign) {
    _initializeCallsignMap();

    callsign = callsign.toUpperCase().trim();

    // Try exact matches first (sorted by length, longest first for better matching)
    final sortedKeys = _callsignToPrefix.keys.toList()
      ..sort((a, b) => b.length.compareTo(a.length));

    for (final prefix in sortedKeys) {
      if (callsign.startsWith(prefix)) {
        final mainPrefix = _callsignToPrefix[prefix]!;
        return _countryData[mainPrefix]?.ituZone;
      }
    }

    return null;
  }

  /// Get country data for a given callsign
  static CountryData? getCountryData(String callsign) {
    _initializeCallsignMap();

    callsign = callsign.toUpperCase().trim();

    final sortedKeys = _callsignToPrefix.keys.toList()
      ..sort((a, b) => b.length.compareTo(a.length));

    for (final prefix in sortedKeys) {
      if (callsign.startsWith(prefix)) {
        final mainPrefix = _callsignToPrefix[prefix]!;
        return _countryData[mainPrefix];
      }
    }

    return null;
  }

  /// Get all countries
  static Map<String, CountryData> getAllCountries() {
    return Map.unmodifiable(_countryData);
  }

  /// Search countries by name
  static List<CountryData> searchByName(String name) {
    name = name.toLowerCase();
    return _countryData.values
        .where((country) => country.name.toLowerCase().contains(name))
        .toList();
  }

  /// Get countries by ITU zone
  static List<CountryData> getCountriesByItuZone(String ituZone) {
    return _countryData.values
        .where((country) => country.ituZone == ituZone)
        .toList();
  }

  /// Get countries by continent
  static List<CountryData> getCountriesByContinent(String continent) {
    return _countryData.values
        .where((country) => country.continent == continent)
        .toList();
  }
